        /// <summary>
        /// 替换内容里的图片Src路径
        /// </summary>
        /// <param name="context"></param>
        /// <returns></returns>
        public static string ReplaceImageSrc(string context, string filePath)
        {
            var pattern = "(?<=<img[^>]*src=\")/?(?<a>/upload[^\"]+)";
            var replaceValue = Regex.Replace(context, pattern, "" + filePath + "$1");
            return replaceValue;
        }

        public static Hashtable GetMsgByXML(string Xml)
        {
            Hashtable hashtable = new Hashtable();
            XmlDocument document = new XmlDocument();
            document.LoadXml(Xml);
            foreach (XmlNode node in document.ChildNodes[0])
            {
                hashtable.Add(node.Name, node.InnerText);
            }
            return hashtable;
        }

